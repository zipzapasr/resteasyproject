            (function () {
                var layers = document.querySelectorAll('.main-slider .image-layer[data-bg]');
                if (!layers.length) return;

                function applyBg(layer) {
                    if (layer.dataset.bgLoaded === '1') return;
                    var desktopBg = layer.getAttribute('data-bg');
                    var mobileBg = layer.getAttribute('data-bg-mobile');
                    if (desktopBg) {
                        layer.style.backgroundImage = 'url(' + desktopBg + ')';
                    }
                    if (mobileBg) {
                        layer.style.setProperty('--slide-bg-mobile', 'url(' + mobileBg + ')');
                    }
                    layer.dataset.bgLoaded = '1';
                }

                if (!('IntersectionObserver' in window)) {
                    layers.forEach(applyBg);
                    return;
                }

                var observer = new IntersectionObserver(function (entries) {
                    entries.forEach(function (entry) {
                        if (!entry.isIntersecting) return;
                        applyBg(entry.target);
                        observer.unobserve(entry.target);
                    });
                }, { rootMargin: '400px 0px' });

                layers.forEach(function (layer) {
                    observer.observe(layer);
                });
            })();
        </script>



